def create_docx_with_content(filepath: Path, title: str, content_lines: list):
    """创建包含内容的Word文档"""
    doc = Document()
    
    # 添加标题
    title_para = doc.add_heading(title, level=1)
    
    # 添加内容
    for line in content_lines:
        if line.strip().startswith('#'):
            # 子标题
            level = line.count('#')
            doc.add_heading(line.strip('# '), level=min(level, 3))
        else:
            # 普通段落
            para = doc.add_paragraph(line)
    
    doc.save(filepath)
    print(f"已创建文档: {filepath}")

